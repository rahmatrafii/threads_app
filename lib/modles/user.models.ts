import mongoose from "mongoose";

// ? membuat struktur data
const userSchema = new mongoose.Schema({
  id: {
    type: String,
    required: true,
  },
  username: {
    type: String,
    unique: true,
    required: true,
  },
  name: {
    type: String,
    required: true,
  },
  image: String,
  bio: String,
  // ? relasi ke scheema threads
  // ? menyimpan ke array, agar setiap user bisa memiliki banyak threads
  threads: [
    {
      type: mongoose.Schema.Types.ObjectId,
      ref: "Thread",
    },
  ],
  onboarded: {
    type: Boolean,
    default: false,
  },
  // ? relasi ke scheema communities
  // ? // ? menyimpan ke array, agar setiap user bisa memiliki banyak communities
  communities: [
    {
      type: mongoose.Schema.Types.ObjectId,
      ref: "Community",
    },
  ],
});

const User = mongoose.models.User || mongoose.model("User", userSchema);

export default User;
